/// Extract raw proof bytes and public inputs from the proof blob.
///
/// Proof blob layout: [256 bytes groth16 proof][N * 32 bytes public inputs]
/// - proof: a(G1, 64) + b(G2, 128) + c(G1, 64) = 256 bytes
/// - public inputs: 4 * 32 = 128 bytes
///   [0] commit
///   [1] seed_cranker
///   [2] seed_player
///   [3] resultado
pub(crate) fn extract(env: &Env, proof: &Bytes) -> (Bytes, [U256; ZK_PUBLIC_INPUTS]) {
    let input_bytes = (ZK_PUBLIC_INPUTS as u32) * 32;
    let proof_len = proof.len() - input_bytes;
    let raw = proof.slice(0..proof_len);

    let mut inputs = [
        U256::from_u32(env, 0),
        U256::from_u32(env, 0),
        U256::from_u32(env, 0),
        U256::from_u32(env, 0),
    ];

    for i in 0..ZK_PUBLIC_INPUTS as u32 {
        let offset = proof_len + i * 32;
        let bytes: BytesN<32> = proof.slice(offset..offset + 32).try_into().unwrap();
        inputs[i as usize] = U256::from_be_bytes(env, &Bytes::from_slice(env, &bytes.to_array()));
    }

    (raw, inputs)
}
